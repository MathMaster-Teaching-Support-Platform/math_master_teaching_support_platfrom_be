package com.fptu.math_master.enums;

/**
 * Difficulty/experience level of a course, shown prominently on the course landing page
 * to help students choose the right course for their background.
 */
public enum CourseLevel {
  /** No prior knowledge required. */
  BEGINNER,
  /** Some foundational knowledge expected. */
  INTERMEDIATE,
  /** Significant prior experience required. */
  ADVANCED,
  /** Suitable for all levels. */
  ALL_LEVELS
}
